<h3>Imprimer son propre un plateau</h3>
<p>
Pour imprimer votre plateau, téléchargez tout d'abord les PDF suivants :<br>
<ul>
	<li><a target='_blank' href='ressources/pion.pdf'>Pions</a></li>
	<li><a target='_blank' href='ressources/plateauGauche.pdf'>Plateau partie gauche</a></li>
	<li><a target='_blank' href='ressources/plateauDroite.pdf'>Plateau partie droite</a></li>
</ul>

Par la suite, imprimez ces différents PDF et collez-les sur des feuilles aimantées (d'au moins 1mm pour les pions).<br><br>
Découpez par la suite les différents pions, et rassemblez les 2 parties du plateau (du simple scotch suffit).
</p>